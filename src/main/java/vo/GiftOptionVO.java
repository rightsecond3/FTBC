package vo;

public class GiftOptionVO {
	private String gop_option       =null;
	private int    gop_ammount      =0;
	private String gop_code         =null;
	private String gift_code        =null;
	private String gop_description  =null;
	public String getGop_option() {
		return gop_option;
	}
	public void setGop_option(String gop_option) {
		this.gop_option = gop_option;
	}
	public int getGop_ammount() {
		return gop_ammount;
	}
	public void setGop_ammount(int gop_ammount) {
		this.gop_ammount = gop_ammount;
	}
	public String getGop_code() {
		return gop_code;
	}
	public void setGop_code(String gop_code) {
		this.gop_code = gop_code;
	}
	public String getGift_code() {
		return gift_code;
	}
	public void setGift_code(String gift_code) {
		this.gift_code = gift_code;
	}
	public String getGop_description() {
		return gop_description;
	}
	public void setGop_description(String gop_description) {
		this.gop_description = gop_description;
	}
}                               
